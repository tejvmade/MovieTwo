package com.example.movietwo.models;



public class VideoTrailer {

    private String id;
    private String key;
    private String name;
    private String site;
    private String size;
    private String type;


    public String getId() {
        return id;
    }

    public String getKey() {
        return key;
    }

    public String getName() {
        return name;
    }

    public String getSite() {
        return site;
    }

    public String getSize() {
        return size;
    }

    public String getType() {
        return type;
    }
}
